"""Summarize paper-aligned ablation results into CSV and LaTeX tables."""
from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path
from typing import Any, Optional, Tuple


FLAG_COLUMNS = [
    ("multi_source", "Multi-source"),
    ("adaptive_topology", "Adaptive topology"),
    ("relation_graph", "Relation graph"),
    ("multi_scale_temporal", "Multi-scale temporal"),
    ("part_pooling", "Part pooling"),
]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--plan", required=True, help="ablation_plan.csv generated by create_ablation_configs.py")
    parser.add_argument("--output-csv", required=True, help="Path for the summary CSV.")
    parser.add_argument("--output-tex", required=True, help="Path for the LaTeX tabular fragment.")
    parser.add_argument("--metrics-subdir", default="val_eval", help="Evaluation subdirectory under each output_dir.")
    parser.add_argument("--digits", type=int, default=2, help="Decimal places for Top-1/Top-5 percentages.")
    return parser.parse_args()


def read_plan(path: Path) -> list[dict[str, str]]:
    with path.open("r", encoding="utf-8", newline="") as handle:
        rows = list(csv.DictReader(handle))
    rows.sort(key=lambda item: int(item.get("order", "0") or 0))
    return rows


def read_json(path: Path) -> Optional[dict[str, Any]]:
    if not path.exists() or path.stat().st_size <= 0:
        return None
    with path.open("r", encoding="utf-8") as handle:
        payload = json.load(handle)
    return payload if isinstance(payload, dict) else None


def read_best_history(path: Path) -> Tuple[Optional[float], Optional[float]]:
    if not path.exists() or path.stat().st_size <= 0:
        return None, None
    with path.open("r", encoding="utf-8", newline="") as handle:
        rows = list(csv.DictReader(handle))
    best_top1: Optional[float] = None
    best_top5: Optional[float] = None
    for row in rows:
        raw_top1 = row.get("val_top1")
        if raw_top1 is None or raw_top1 == "":
            continue
        top1 = float(raw_top1)
        top5 = float(row["val_top5"]) if row.get("val_top5") not in {None, ""} else None
        if best_top1 is None or top1 > best_top1:
            best_top1 = top1
            best_top5 = top5
    return best_top1, best_top5


def load_metrics(output_dir: Path, metrics_subdir: str) -> Tuple[Optional[float], Optional[float], str]:
    eval_metrics = read_json(output_dir / metrics_subdir / "metrics.json")
    if eval_metrics is not None:
        return (
            float(eval_metrics["top1"]) if eval_metrics.get("top1") is not None else None,
            float(eval_metrics["top5"]) if eval_metrics.get("top5") is not None else None,
            str(output_dir / metrics_subdir / "metrics.json"),
        )

    soup_metrics = read_json(output_dir / "model_soup.json")
    if soup_metrics is not None and soup_metrics.get("soup_val_top1") is not None:
        return (
            float(soup_metrics["soup_val_top1"]),
            float(soup_metrics["soup_val_top5"]) if soup_metrics.get("soup_val_top5") is not None else None,
            str(output_dir / "model_soup.json"),
        )

    history_top1, history_top5 = read_best_history(output_dir / "history.csv")
    if history_top1 is not None:
        return history_top1, history_top5, str(output_dir / "history.csv")
    return None, None, "missing"


def mark(value: str) -> str:
    return r"\checkmark" if str(value).strip() in {"1", "true", "True", "yes"} else "--"


def metric_text(value: Optional[float], digits: int) -> str:
    if value is None:
        return "PENDING"
    return f"{value * 100.0:.{digits}f}"


def write_tex(rows: list[dict[str, str]], path: Path) -> None:
    header = (
        r"\begin{tabular}{lccccccc}" "\n"
        r"\toprule" "\n"
        r"Setting & Multi-source & Adaptive topology & Relation graph & Multi-scale temporal & Part pooling & Top-1 & Top-5 \\"
        "\n"
        r"\midrule" "\n"
    )
    body_lines = []
    for row in rows:
        values = [
            row["setting"],
            row["multi_source"],
            row["adaptive_topology"],
            row["relation_graph"],
            row["multi_scale_temporal"],
            row["part_pooling"],
            row["top1"],
            row["top5"],
        ]
        body_lines.append(" & ".join(values) + r" \\")
    footer = "\n" + r"\bottomrule" + "\n" + r"\end{tabular}" + "\n"
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(header + "\n".join(body_lines) + footer, encoding="utf-8")


def main() -> None:
    args = parse_args()
    plan_rows = read_plan(Path(args.plan))
    summary_rows: list[dict[str, str]] = []
    for row in plan_rows:
        output_dir = Path(row["output_dir"])
        top1, top5, source = load_metrics(output_dir, args.metrics_subdir)
        summary_row = {
            "dataset": row["dataset"],
            "order": row["order"],
            "variant": row["variant"],
            "setting": row["setting"],
            "top1": metric_text(top1, args.digits),
            "top5": metric_text(top5, args.digits),
            "metrics_source": source,
        }
        for key, _ in FLAG_COLUMNS:
            summary_row[key] = mark(row[key])
        summary_rows.append(summary_row)

    output_csv = Path(args.output_csv)
    output_csv.parent.mkdir(parents=True, exist_ok=True)
    fieldnames = [
        "dataset",
        "order",
        "variant",
        "setting",
        *[key for key, _ in FLAG_COLUMNS],
        "top1",
        "top5",
        "metrics_source",
    ]
    with output_csv.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(summary_rows)
    write_tex(summary_rows, Path(args.output_tex))
    print(f"Wrote summary CSV: {output_csv}")
    print(f"Wrote LaTeX table: {args.output_tex}")


if __name__ == "__main__":
    main()
